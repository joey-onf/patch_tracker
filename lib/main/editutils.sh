## -----------------------------------------------------------------------
## Intent: Display an error mmessage then exit
## -----------------------------------------------------------------------
function add2edit()
{
    [[ ! -v files2edit ]] && { declare -g -a files2edit=(); }
    files2edit+=("$@")
    return
}

## -----------------------------------------------------------------------
## Intent: Edit files in the queue
## -----------------------------------------------------------------------
function edit_files()
{
    if [[ -v files2edit ]]; then
        emacs "${files2edit[@]}" &
    fi

    return
}

